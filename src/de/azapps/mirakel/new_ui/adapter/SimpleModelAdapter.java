package de.azapps.mirakel.new_ui.adapter;

import android.content.Context;
import android.database.Cursor;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.CursorAdapter;
import android.widget.TextView;

import de.azapps.mirakel.model.ModelBase;
import de.azapps.mirakel.model.list.ListMirakel;

public class SimpleModelAdapter extends CursorAdapter {
    private LayoutInflater mInflater;

    public SimpleModelAdapter(Context context, Cursor c, int flags) {
        super(context, c, flags);
        mInflater = (LayoutInflater) context.getSystemService(Context.LAYOUT_INFLATER_SERVICE);
    }

    @Override
    public View newView(Context context, Cursor cursor, ViewGroup parent) {
        View view = mInflater.inflate(android.R.layout.simple_list_item_1, null);
        ViewHolder viewHolder = new ViewHolder(view);
        view.setTag(viewHolder);
        return view;
    }

    @Override
    public void bindView(View view, Context context, Cursor cursor) {
        ViewHolder viewHolder = (ViewHolder) view.getTag();
        viewHolder.id = cursor.getLong(cursor.getColumnIndex(ModelBase.ID));
        viewHolder.name.setText(cursor.getString(cursor.getColumnIndex(ModelBase.NAME)));
    }

    public static class ViewHolder {
        private final TextView name;
        private long id;

        private ViewHolder(View view) {
            name = (TextView) view.findViewById(android.R.id.text1);
        }

        public long getId() {
            return id;
        }
    }
}
